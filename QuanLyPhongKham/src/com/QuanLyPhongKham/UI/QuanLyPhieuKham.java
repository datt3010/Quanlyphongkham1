    private void setform(PhieuKham pk) {
        lblNgayKham1.setText(XDate.toString(pk.getNgaykham(), "dd/MM/yyyy"));
        txtBS.setText(pk.getMabacsy());
        txtBenhNhan.setText(String.valueOf(pk.getMabenhnhan()));
        txtNhanVien.setText(pk.getManhanvien());
        cldNgayTaiKham.setDate(pk.getNgaytaikham());
        txaKetLuanBacSi.setText(pk.getKetluan());
    }

    private void edit() {
        int maphieukham = (int) tblDSPhieuKham.getValueAt(row, 0);
        PhieuKham pk = pkdao.SelectByID(maphieukham);
        this.setform(pk);
        this.updateStatus();
    }

    private void insert() {
        if (this.Error()) {
            try {
                PhieuKham pk = getform();
                pkdao.insert(pk);
                this.filltable();
                MsgBox.alert(this, "Insert thành công!!");
                this.clearForm();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

    private void update() {
        if (this.row == -1) {
            MsgBox.alert(this, "Vui lòng chọn phiếu khám cần update!!!");
            return;
        }
        if (this.Error()) {
            try {
                PhieuKham pk = getformUpdate();
                pkdao.update(pk);
                this.filltable();
                MsgBox.alert(this, "Update thành công!!");
                this.clearForm();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

    private void delete() {
        try {
            if (this.row == -1) {
                MsgBox.alert(this, "Vui lòng chọn phiếu khám cần xoá!!!");
                return;
            } else {
                row = tblDSPhieuKham.getSelectedRow();
                int maphieukham = (int) tblDSPhieuKham.getValueAt(row, 0);
                if (MsgBox.confirm(this, "Bạn có muốn xoá phiếu khám này không?")) {
                    pkdao.delete(maphieukham);
                    this.filltable();
                    MsgBox.alert(this, "Delete thành công!!");
                    this.clearForm();

                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void clearForm() {
        PhieuKham pk = new PhieuKham();
        this.setform(pk);
        this.row = -1;
        this.updateStatus();
    }

    private boolean Error() {
        if (Validation.isempty(txtBS, "Mã Bác Sĩ đang rỗng!!") == false) {
            return false;
        }
        if (Validation.isempty(txtBenhNhan, "Mã Bệnh Nhân đang rỗng!!") == false) {
            return false;
        }

        if (Validation.isemptyTXA(txaKetLuanBacSi, "Kết luận của Bác Sĩ đang rỗng!!") == false) {
            return false;
        }
        return true;
    }

    private void updateStatus() {
        boolean edit = (this.row >= 0); //Trả về True nếu đang ở hàng lớn hơn 0
        boolean first = (this.row == 0); //Trả về True nếu đang ở hàng đầu tiên
        boolean last = (this.row == tblDSPhieuKham.getRowCount() - 1); //Trả về True nếu đang ở hàng cuối cùng

        btnThem.setEnabled(!edit);
        btnMoi.setEnabled(edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
    }
    
